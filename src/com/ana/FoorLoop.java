package com.ana;

public class FoorLoop {
    public static void main(String[] args) {

        /*
        Primero se declara la condición de comienzo
        una variable, se suele usar i,j,k,l...
        luego tenemos la condición de parada
        luego tenemos la condición de paso, como se va a ir
        incrementando el bucle
         */
        for (int i = 0; i <=  10; i++){
            System.out.println(i + " Hola mundo");//He sacado el valor de i para saber cuantas veces se repite
        }
    }
}
